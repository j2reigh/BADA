// Browser language detection
export function detectUILanguage(): UILanguage {
  const lang = navigator.language.toLowerCase();
  // Chinese variants need special handling
  if (lang.startsWith('zh-tw') || lang.startsWith('zh-hant')) return 'zh-TW';
  if (lang.startsWith('zh')) return 'zh-CN';
  const base = lang.split('-')[0];
  const map: Record<string, UILanguage> = {
    ko: 'ko', id: 'id', ms: 'ms', th: 'th',
    vi: 'vi', tl: 'tl', fil: 'tl', ja: 'ja', hi: 'hi',
  };
  return map[base] || 'en';
}

